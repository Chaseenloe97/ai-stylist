// Helper function to get products by category and style
export const getProductsByStyle = (category, style) => {
  if (!products[category]) return [];
  if (!products[category][style]) return products[category].minimalist || [];
  return products[category][style];
};

// Get random products across categories
export const getRandomProducts = (count = 3, style = 'minimalist') => {
  const allProducts = [
    ...getProductsByStyle('tops', style),
    ...getProductsByStyle('bottoms', style),
    ...getProductsByStyle('shoes', style),
    ...getProductsByStyle('outerwear', style),
  ];

  return allProducts.sort(() => 0.5 - Math.random()).slice(0, count);
};
